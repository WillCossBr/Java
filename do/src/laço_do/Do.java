package laço_do;

public class Do
{
	public static void main(String[] args)
	{
		/*contador de 0 até 10*/
		
		/*variável*/
		int contador = 0;
		
		System.out.println("Contador de 0 até 10");
		
		/*Laço de repetição Do
		 * primeiro o comando depois a condição*/
		do
		{
			System.out.println(contador);
			contador ++;
		}while(contador < 11);
		
	}
	
}
